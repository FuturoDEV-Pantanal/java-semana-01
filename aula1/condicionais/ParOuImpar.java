package condicionais;

public class ParOuImpar {
    public static void main(String[] args) {
      
      int number = 7;
      
      if (number % 2 == 0) { // Verifica se o número é par
        System.out.println(number + " é par");
      } else {
        System.out.println(number + " é ímpar");
      }
    }
}
